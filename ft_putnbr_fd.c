#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	int		sign;

	sign = 1;
	if (n == '-')
	{
		sign *= -1;
	}
	else if (n >= '0' && n <= '9')
		ft_putchar_fd(n, fd);
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
}
